CREATE TABLE idemnite (
    id INT IDENTITY (1, 1),
    montant INT,
    site_id INT,
    catg_id INT,
    rmq_id INT,
    sousTypeDoc_id INT,
    date_creation DATE NOT NULL,
    date_modification DATE NOT NULL,
    CONSTRAINT PK_idemnitie PRIMARY KEY (id),
    CONSTRAINT FK_idemnite_catg_id FOREIGN KEY (catg_id) REFERENCES Catg (id),
    CONSTRAINT FK_idemnite_site_id FOREIGN KEY (site_id) REFERENCES Site (id),
    CONSTRAINT FK_idemnite_rmq_id FOREIGN KEY (rmq_id) REFERENCES Rmq (id),
    CONSTRAINT FK_idemnite_sousTypeDoc_id FOREIGN KEY (sousTypeDoc_id) REFERENCES Sous_type_document (ID_Sous_Type_Document)
);

